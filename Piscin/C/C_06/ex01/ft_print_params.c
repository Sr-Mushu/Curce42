#include <unistd.h>
int	main(int ac, char **ag)
{
	int	k;
	int	y;
	int	t;

	t = ac - 1;
	y = 1;
	while (y < t)
	{
		k = 0;
		while (ag[y][k] != '\0')
		{
			write (1, &ag[y][k], 1);
			k++;
		}
		write (1, "\n", 1);
		y++;
	}
}
