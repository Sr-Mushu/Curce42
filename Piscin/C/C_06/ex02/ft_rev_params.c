#include <unistd.h>
int	main(int ac, char **ag)
{
	int	k;
	int	y;
	int	t;

	t = ac;
	y = 1;
	while (t > y)
	{
		k = 0;
		while (ag[t][k] != '\0')
		{
			write (1, &ag[t][k], 1);
			k++;
		}
		write (1, "\n", 1);
		t--;
	}
}
