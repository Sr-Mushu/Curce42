char	*ft_strupcase(char *str)
{
	int	k;

	k = 0;
	while (str[k])
	{
		if (!((str[k] <= '`') || (str[k] >= '{')))
			str[k] = str[k] - 32;
		k++;
	}
	return (str);
}
